    private void jBEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBEliminarActionPerformed
                                         
    int filaSeleccionada = jTablaClienteCompleto.getSelectedRow();

    if (filaSeleccionada >= 0) {
        int confirmacion = JOptionPane.showConfirmDialog(null, "¿Está seguro de eliminar el cliente?", "Confirmar eliminación", JOptionPane.YES_NO_OPTION);

        if (confirmacion == JOptionPane.YES_OPTION) {
            accionesTabla.eliminarCliente(filaSeleccionada, jTablaClienteCompleto, modelo, cn);
            
            jTablaClienteCompleto.clearSelection();
            
            if (trsfiltro != null) trsfiltro.setRowFilter(null);
            if (trsfiltro1 != null) trsfiltro1.setRowFilter(null);
            if (trsfiltro2 != null) trsfiltro2.setRowFilter(null);
            
            jTBuscarNombre.setText("");
            jTBuscarCodigo.setText("");
            jTBuscarCedula.setText("");
        }
    } else {
        JOptionPane.showMessageDialog(null, "Seleccione una fila para eliminar", "Advertencia", JOptionPane.WARNING_MESSAGE);
    }
    }//GEN-LAST:event_jBEliminarActionPerformed

