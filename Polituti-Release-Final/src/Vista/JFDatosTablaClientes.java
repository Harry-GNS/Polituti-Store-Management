    private void jBActualizarDatosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBActualizarDatosActionPerformed
      String nuevoNombre = jFNombreEditar.getText();
    String nuevoPrecio = jFCedulaEditar.getText();
    String nuevaCantidad = jFTelefonoEditar.getText();

    int filaSeleccionada = jTablaClienteCompleto.getSelectedRow();
    
    if (filaSeleccionada >= 0) {
        // Llamar al método de actualización
        accionesTabla.manejarActualizacionCliente(filaSeleccionada, nuevoNombre, nuevoPrecio, nuevaCantidad, jTablaClienteCompleto, panelEditar);
        
        // Limpiar selección de la tabla
        jTablaClienteCompleto.clearSelection();

        // Limpiar filtros
        if (trsfiltro != null) trsfiltro.setRowFilter(null);
        if (trsfiltro1 != null) trsfiltro1.setRowFilter(null);
        if (trsfiltro2 != null) trsfiltro2.setRowFilter(null);

        // Limpiar campos de texto
        jTBuscarNombre.setText("");
        jTBuscarCodigo.setText("");
        jTBuscarCedula.setText("");

        // Ocultar panel de edición y botón de actualizar datos
        this.panelEditar.setVisible(false);
        this.jBActualizarDatos.setVisible(false);
    } else {
        JOptionPane.showMessageDialog(null, "Seleccione una fila para actualizar", "Advertencia", JOptionPane.WARNING_MESSAGE);
    }

    }//GEN-LAST:event_jBActualizarDatosActionPerformed

